# Exemplos de Strings

nome = "João da Silva"
curso = "TDS"

print(type(nome))
print(type(curso))

# Acessar um elemento (caractere) da string

print(nome[5])

# Acessar um "pedaço" (conj. de caracteres) da String

print(curso[0:5])

